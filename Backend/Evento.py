class Evento:
    def __init__(self, fecha, usuario, afectado, codigo, descripcionCodigo):
        # str
        self.fecha = fecha
        # str
        self.usuario = usuario
        # objeto Afecto
        self.afectado = afectado
        # int
        self.codigo = codigo
        # str
        self.descripcionCodigo = descripcionCodigo

